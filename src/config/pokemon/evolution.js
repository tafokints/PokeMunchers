function processEvolutionData() {
    // First, ensure all numbers are padded to 3 digits
    const paddedEvolutionData = {};
    Object.entries(evolutionData).forEach(([number, data]) => {
        const paddedNumber = number.padStart(3, '0');
        paddedEvolutionData[paddedNumber] = {
            ...data,
            evolvesFrom: null,  // Reset all evolvesFrom
            evolvesTo: data.evolvesTo.map(evo => ({
                ...evo,
                number: evo.number.padStart(3, '0')  // Pad evolution numbers
            }))
        };
    });

    // Then process evolvesFrom relationships
    Object.entries(paddedEvolutionData).forEach(([number, data]) => {
        if (Array.isArray(data.evolvesTo)) {
            data.evolvesTo.forEach(evolution => {
                if (evolution && evolution.number && paddedEvolutionData[evolution.number]) {
                    paddedEvolutionData[evolution.number].evolvesFrom = number;
                }
            });
        }
    });

    // Replace the original data with processed data
    Object.assign(evolutionData, paddedEvolutionData);
}

